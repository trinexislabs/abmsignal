export interface PlaybookContext {
  playbookId: string
  productName: string
  productDescription: string
  valuePropositions: string[]
  targetCompany: string
  industry: string
  geography: string
  priorityTier: string
  productUrl?: string
  competitors?: string[]
  deploymentModel?: string
  dealSize?: string
  salesCycle?: string
}

export interface WritingContext extends PlaybookContext {
  approvedContacts: {
    name: string
    title: string
    company: string
    linkedin_url?: string
    confidence: string
    rationale?: string
  }[]
}

