/**
 * Build a writing prompt for a subset of sections (batch mode).
 * Split writing across two calls to stay within model output token limits.
 */
export function buildWritingPromptBatch(
  ctx: WritingContext,
  batchKeys: readonly string[],
  batchNum: 1 | 2,
): string {
  const contactsSummary = ctx.approvedContacts
    .map(c => `- ${c.name} (${c.title}) — ${c.rationale ?? 'Key decision maker'}`)
    .join('\n')

  const instructions = sectionInstructions(ctx)
  const sectionLines: string[] = []
  batchKeys.forEach((key, idx) => {
    const globalOrder = batchNum === 1 ? idx + 1 : idx + 10
    sectionLines.push(`${globalOrder}. ${key}`)
    const lines = instructions[key] ?? []
    lines.forEach(l => sectionLines.push(`   - ${l}`))
    sectionLines.push('')
  })

  const batchDesc = batchNum === 1
    ? `BATCH 1 of 2 — Sections 1–9 (Research + Positioning)`
    : `BATCH 2 of 2 — Sections 10–18 (Outreach + Execution)`

  const batchNote = batchNum === 2
    ? `NOTE: This is Batch 2. Sections 1–9 are already written. Be consistent with that content.`
    : ''

  return [
    `# ABM PLAYBOOK WRITING — Phase 2 (${batchDesc})`,
    ``,
    ...(batchNote ? [`## ${batchNote}`, ``] : []),
    `## Your Task`,
    `Write the ${batchKeys.length} assigned sections of the ABM playbook for ${ctx.targetCompany}.`,
    `Every section must be deeply researched, specific, and actionable — NOT generic.`,
    ``,
    `## CRITICAL RULES — READ CAREFULLY`,
    `- DO NOT call any external APIs or HTTP endpoints.`,
    `- DO NOT use curl, fetch, or any network tools to update application state.`,
    `- DO NOT write to any files or databases directly.`,
    `- Your ONLY output must be a single JSON block at the end of your response.`,
    `- The JSON must match the exact schema defined below.`,
    ``,
    `## Context`,
    `### Product: ${ctx.productName}`,
    `Description: ${ctx.productDescription}`,
    `Value Props: ${ctx.valuePropositions.join('; ')}`,
    `Competitors: ${ctx.competitors?.join(', ') ?? 'N/A'}`,
    `Deployment: ${ctx.deploymentModel ?? 'SaaS'} | Deal Size: ${ctx.dealSize ?? 'N/A'} | Sales Cycle: ${ctx.salesCycle ?? 'N/A'}`,
    ``,
    `### Target Account: ${ctx.targetCompany}`,
    `Industry: ${ctx.industry} | Geography: ${ctx.geography} | Priority: ${ctx.priorityTier}`,
    ``,
    `### Approved Contacts`,
    contactsSummary,
    ``,
    `## Sections to Write (${batchKeys.length} sections)`,
    ``,
    ...sectionLines,
    `## Global Writing Instructions`,
    `- Every section must be specific to ${ctx.targetCompany} — zero generic filler.`,
    `- Reference approved contacts by name throughout.`,
    `- Use markdown formatting: ## headers, ### sub-headers, tables, bullet lists.`,
    `- For every factual claim, include (source: URL) inline citation.`,
    `- Cultural context and tone must match ${ctx.geography} norms throughout.`,
    `- Minimum 300 words per section. personalized_sequences and roi_model should be 800+ words.`,
    ``,
    `## Required Output Format`,
    `End your response with a JSON block using EXACTLY this schema:`,
    ``,
    `\`\`\`json`,
    `{`,
    `  "phase": "writing",`,
    `  "status": "complete",`,
    `  "progress_pct": ${batchNum === 1 ? 80 : 100},`,
    `  "agent_status": "Batch ${batchNum} complete",`,
    `  "sections": [`,
    `    {`,
    `      "section_key": "${batchKeys[0]}",`,
    `      "title": "Section Title",`,
    `      "content_markdown": "## Overview\\n\\n...",`,
    `      "order_index": ${batchNum === 1 ? 1 : 10},`,
    `      "sources": [`,
    `        {`,
    `          "url": "https://...",`,
    `          "title": "Source",`,
    `          "publisher": "Publisher",`,
    `          "claim": "The specific claim this supports",`,
    `          "confidence": "high"`,
    `        }`,
    `      ]`,
    `    }`,
    `  ]`,
    `}`,
    `\`\`\``,
    ``,
    `All ${batchKeys.length} assigned sections must be present. Content must be specific to ${ctx.targetCompany}.`,
  ].join('\n')
}
